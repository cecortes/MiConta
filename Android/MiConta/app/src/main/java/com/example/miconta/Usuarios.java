package com.example.miconta;

public class Usuarios {

    //Atributos
    String usr_correo;
    String usr_pwd;
    String usr_created;

    //Get & Set
    public String getUsr_correo() {
        return usr_correo;
    }

    public void setUsr_correo(String usr_correo) {
        this.usr_correo = usr_correo;
    }

    public String getUsr_pwd() {
        return usr_pwd;
    }

    public void setUsr_pwd(String usr_pwd) {
        this.usr_pwd = usr_pwd;
    }

    public String getUsr_created() {
        return usr_created;
    }

    public void setUsr_created(String usr_created) {
        this.usr_created = usr_created;
    }
}
